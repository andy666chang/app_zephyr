#define M03X_MFP_SHIFT 0U
#define M03X_MFP_MASK  0xFU
#define M03X_PIN_SHIFT 4U
#define M03X_PIN_MASK  0xFU
#define M03X_PORT_SHIFT 8U
#define M03X_PORT_MASK  0xFU

/**
 * @brief Pin configuration configuration bit field.
 *
 * Fields:
 *
 * - mfp [ 0 : 3 ]
 * - pin [ 4 : 7 ]
 * - port [ 8 : 11 ]
 *
 * @param port Port ('A'..'H')
 * @param pin  Pin (0..15)
 * @param mfp  Multi-function value (0..15)
 */
#define M03X_PINMUX(port, pin, mfp)					\
	(((((port) - 'A') & M03X_PORT_MASK) << M03X_PORT_SHIFT) |	\
	(((pin) & M03X_PIN_MASK) << M03X_PIN_SHIFT) |		\
	(((mfp) & M03X_MFP_MASK) << M03X_MFP_SHIFT))

#define M03X_PORT(pinmux) \
	(((pinmux) >> M03X_PORT_SHIFT) & M03X_PORT_MASK)
#define M03X_PIN(pinmux) \
	(((pinmux) >> M03X_PIN_SHIFT) & M03X_PIN_MASK)
#define M03X_MFP(pinmux) \
	(((pinmux) >> M03X_MFP_SHIFT) & M03X_MFP_MASK)


